public class Whrc {
    private int w;
    private int h;
    private int r;
    private int c;

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public Whrc(int w, int h, int r, int c) {
        this.w=w;
        this.h=h;
        this.r=r;
        this.c=c;
    }
}
